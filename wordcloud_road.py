# 1음절이지만 의미 있는 지리/자연 명사
ONE_CHAR_GEO = {'산', '강', '물', '숲', '섬', '골', '천', '호', '봉', '령'}
# 조사 제거 후 남는 동사/형용사 어간 → 버림
VERB_STEMS = {
    '흐르', '기원하', '생겼다', '되었다', '있었다', '불리었다',
    '만들어', '형성되', '위치하', '연결하', '통과하', '지나가',
    '붙여지', '불려지', '이루어', '가로지르',
}

def tokenize(text):
    text = noise_pat.sub(' ', text)
    tokens = re.split(r'\s+', text.strip())
    result = []
    for t in tokens:
        if not re.fullmatch(r'[가-힣]+', t):
            continue
        if len(t) < 2 or len(t) > 8:
            continue
        if t in STOP:
            continue
        normalized = strip_josa(t)
        # 1글자로 줄어든 경우: 지리/자연 명사만 허용
        if len(normalized) == 1:
            if normalized in ONE_CHAR_GEO:
                result.append(normalized)
            continue
        if normalized in STOP or normalized in STOP_EXTRA or normalized in VERB_STEMS:
            continue
        result.append(normalized)
    return result

