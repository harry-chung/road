noise_pat = re.compile(r'[0-9a-zA-Z,\.·\-\~\(\)\[\]<>\"\'「」『』【】\s]+')

# 명사 뒤에 붙는 조사 제거 (긴 것부터 순서대로)
_JOSA = ['으로부터', '에서부터', '으로서', '이라는', '이라고', '이라서', '이어서',
         '에서의', '으로는', '에서는', '에서도', '이라며', '으로써',
         '에서', '으로', '에는', '에도', '이며', '이고', '이나', '이라',
         '에게', '보다', '마다', '조차', '마저', '처럼',
         '가', '이', '을', '를', '은', '는', '의', '도', '만', '과', '와']

def strip_josa(word):
    """단어 끝 조사를 제거하여 기본형 명사 반환"""
    for j in _JOSA:
        if word.endswith(j):
            stem = word[:-len(j)]
            if len(stem) >= 2 and re.fullmatch(r'[가-힣]+', stem):
                return stem
    return word

